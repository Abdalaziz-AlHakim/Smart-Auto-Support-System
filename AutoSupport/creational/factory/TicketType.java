package creational.factory;

/**
 * Enumeration representing the different categories of support tickets.
 */
public enum TicketType {
    /** A software bug or defect report. */
    BUG,
    /** A customer complaint. */
    COMPLAINT,
    /** A request for a new feature or enhancement. */
    FEATURE_REQUEST,
    /** A general inquiry or question. */
    INQUIRY
}
